//cart change

exports.changeCart = catchAsyncErrors(async (req, res) => {
  try {
    const userId = req.params.id;
    const newCart = req.body.cart;

    // Find the user by ID and update the cart array
    const updatedUser = await User.findByIdAndUpdate(
      userId,
      { cart: newCart },
      { new: true, runValidators: true }
    );

    if (!updatedUser) {
      return res
        .status(404)
        .json({ success: false, message: "User not found" });
    }

    res.status(200).json({ success: true, data: updatedUser.cart });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: "Internal Server Error",
      error: error.message,
    });
  }
});

